"""Регресс-тесты сопоставления «склад Ozon → бизнес-кластер».

Главный кейс: склад МИНСК_МПСЦ (кластер «Беларусь») раньше молча уезжал в
«Новосибирск», потому что аббревиатура «нск» входила подстрокой в слово «минск».
Из-за этого весь белорусский остаток показывался нулём в разделе «Отгрузки FBO»,
а Новосибирску приписывался чужой товар.

Запуск:  python -m pytest modules/fbo/tests -q
"""

from __future__ import annotations

import sqlite3

import pytest

from modules.fbo.storage import init_fbo_db
from modules.fbo.sync import heal_inferred_clusters, infer_cluster, normalize_cluster_name


@pytest.mark.parametrize(
    "warehouse, expected",
    [
        # ── Корень бага: «минск» больше не ловится на «нск» ──────────────
        ("МИНСК_МПСЦ", "Беларусь"),
        ("Минск РФЦ", "Беларусь"),
        # ── Новосибирск по-прежнему распознаётся (и полным словом, и «нск») ─
        ("НОВОСИБИРСК_3_РФЦ", "Новосибирск"),
        ("ТСЦ_НСК", "Новосибирск"),
        # ── Другие жертвы того же «нск»-подстрочного матчинга ─────────────
        ("САРАТОВ_ХАБ_ЕЛШАНСКИЙ", "Саратов"),
        ("РАМЕНСКОЕ_АППЗ_2", "Москва, МО и Дальние регионы"),
        ("СЛАВЯНСКИЙ_МИР_АППЗ_1", "Москва, МО и Дальние регионы"),
        # ── Граница слова не должна была отнять уже работавшие склады ─────
        ("ПЕРВОУРАЛЬСК_АППЗ_1", "Екатеринбург"),
        ("СЦ_ВОЛХОНСКОЕ_ТСЦ_Возвраты", "Санкт-Петербург и СЗО"),
        # ── Побеждает самое длинное совпадение, а не первое по порядку ────
        ("ОМСК_ХАБ_КАЗАХСТАНСКАЯ", "Омск"),  # «омск», а не «казахст»→Астана
        ("КРАСНОДАР_СППЗ_УРАЛЬСКАЯ", "Краснодар"),  # «краснодар», а не «уральск»
        ("СЦ_Московское_Блок_1_Возвраты", "Санкт-Петербург и СЗО"),  # Московское шоссе — Питер
        # ── Кластеры, которые раньше были склеены с чужими ────────────────
        ("ТВЕРЬ_РФЦ", "Тверь"),  # был склеен с Москвой
        ("КРАСНОДАР_РФЦ", "Краснодар"),  # был склеен с Ростовом
        ("ТЮМЕНЬ_РФЦ", "Тюмень"),  # был склеен с Екатеринбургом
        ("ОРЕНБУРГ_РФЦ", "Оренбург"),  # был склеен с Уфой
        ("НЕВИННОМЫССК_РФЦ", "Невинномысск"),  # был склеен с Махачкалой
        ("ЯРОСЛАВЛЬ_РФЦ", "Ярославль"),  # кластера не было в словаре вовсе
        ("ДЗЕРЖИНСК_ТСЦ", "Казань"),  # у Ozon Дзержинск относится к Казани
        # ── Прочие кластеры, чтобы граница слова ничего не сломала ────────
        ("ХОРУГВИНО_РФЦ", "Москва, МО и Дальние регионы"),
        ("СПБ_КОЛПИНО_РФЦ", "Санкт-Петербург и СЗО"),
        ("Екатеринбург_РФЦ_НОВЫЙ", "Екатеринбург"),
        ("КАЗАНЬ_РФЦ_НОВЫЙ", "Казань"),
        ("УФА_РФЦ", "Уфа"),
    ],
)
def test_infer_cluster(warehouse: str, expected: str) -> None:
    assert infer_cluster(warehouse) == expected


def test_normalize_cluster_name_uses_word_boundary() -> None:
    """Латинское имя из постингов и кириллический склад дают один кластер."""
    assert normalize_cluster_name("Belarus`") == "Беларусь"
    assert normalize_cluster_name("МИНСК_МПСЦ") == "Беларусь"


def test_heal_fixes_cached_inferred_row_and_keeps_api_row() -> None:
    """Кеш склада, испорченный старой логикой, чинится сам при следующем синке.

    _get_cluster предпочитает кеш свежему выводу, поэтому без лечения строка
    «МИНСК_МПСЦ → Новосибирск» пережила бы починку infer_cluster.
    Строки source='api' приходят от Ozon и считаются истиной — их не трогаем.
    """
    conn = sqlite3.connect(":memory:")
    conn.row_factory = sqlite3.Row
    init_fbo_db(conn)
    with conn:
        conn.executemany(
            "INSERT INTO fbo_warehouse_cluster_map (warehouse_name, cluster_name, source) "
            "VALUES (?, ?, ?)",
            [
                ("МИНСК_МПСЦ", "Новосибирск", "inferred"),  # испорчено старой логикой
                ("АСТАНА_РФЦ", "Астана", "inferred"),  # корректно — трогать не нужно
                ("ВЫДУМАННЫЙ_СКЛАД", "Новосибирск", "api"),  # истина от Ozon
            ],
        )

    changed = heal_inferred_clusters(conn)

    assert changed == [("МИНСК_МПСЦ", "Новосибирск", "Беларусь")]
    rows = {
        r["warehouse_name"]: r["cluster_name"]
        for r in conn.execute("SELECT warehouse_name, cluster_name FROM fbo_warehouse_cluster_map")
    }
    assert rows["МИНСК_МПСЦ"] == "Беларусь"
    assert rows["АСТАНА_РФЦ"] == "Астана"
    assert rows["ВЫДУМАННЫЙ_СКЛАД"] == "Новосибирск"

    # Повторный прогон уже ничего не меняет (идемпотентность).
    assert heal_inferred_clusters(conn) == []
    conn.close()
